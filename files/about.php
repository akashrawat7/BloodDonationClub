						<center><strong>BLOOD DONATION PROCESS</strong></center><br>                                             
						It is always good to plan blood donation in advance. Consult your doctor before donating blood if there are any health issues or concerns. It is always good to have healthy diet weeks before the donation. On the day of donation, make sure you are well hydrated so keep drinking plenty of fluids. One should wear comfortable clothes during the donation process. In case you are undergoing any treatment or medication, it is advisable to inform the blood bank/clinic/hospital where you are donating blood.<br><br>
