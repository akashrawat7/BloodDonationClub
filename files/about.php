                                                                                               <center><strong>BLOOD DONATION</strong></center><br>
                                                                                         <p>

                                                                                       A blood donation occurs when a person voluntarily has blood drawn and used for transfusions 
                                                                                       and/or made into biopharmaceutical medications by a process called fractionation (separation
                                                                                       of whole-blood components).  Donation may be of whole blood (WB), or of specific components directly (the latter called apheresis).
																					   Blood banks often participate in the collection process as well as the procedures that follow it.<br>

																						Today in the developed world, most blood donors are unpaid volunteers who donate blood for a community supply. 
																						In poorer countries, established supplies are limited and donors usually give blood when family or friends need a transfusion (directed donation).
																						Many donors donate as an act of charity, but in countries that allow paid donation some donors are paid, and in some cases there are incentives other than money such as paid time off from work. 
																						Donors can also have blood drawn for their own future use (autologous donation). Donating is relatively safe, but some donors have  bruising where the needle is inserted or may feel faint.<br>

																						Potential donors are evaluated for anything that might make their blood unsafe to use. The screening includes testing for diseases that can be transmitted by a blood transfusion, 
																						including HIV and viral hepatitis. The donor must also answer questions about medical history and take a short physical examination to make sure the donation is not hazardous to his or her health. 
