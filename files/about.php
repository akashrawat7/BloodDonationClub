						<center>How often can you donate blood?</center><br>
							One has to wait for 56 days or 8 weeks between the whole blood donations. The wait period is 112 days or 16 weeks between power red donations. Avoid donation if suffering from any disorders and consult doctor before doing it.<br><br>
						<center>How much blood do you donate?</center><br>
						You can donate one unit or 350 ml of blood every 8 weeks. Organizations such as American Red Cross organize donation camps Where one can participate and donate blood. You can also donate blood at any hospital.<br><br>
						<center>How old do you have to be to donate blood?</center><br>
							Donor’s age must be between 18-60 years and weight more than 45 kgs to be able to donate blood. Any healthy person can donate blood after the required gap of 56 days. This wait time helps to replenish the blood levels in donor’s body.
